package logic.bean;


public class LoginBean {
	private String userID;
	private String pwd;
	
	public void setUserID(String userID) {
		this.userID = userID;
	}
	
	public void setPwd(String password) {
		this.pwd = password;
	}
	
	public String getUserID() {
		return userID;
	}
	
	public String getPwd() {
		return pwd;
	}
	
	
}
